#!/usr/bin/env python3
"""
Module that provides a single function to perform float addition.

This module demonstrates basic use of type annotations in Python.
"""


def add(a: float, b: float) -> float:
    """Add two float numbers and return their sum.

    Args:
        a (float): The first number.
        b (float): The second number.

    Returns:
        float: The result of adding a and b.
    """
    return a + b

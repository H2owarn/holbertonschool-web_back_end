#!/usr/bin/env python3
"""
Module that provides a single function to returns a concatenated string.

"""


def concat(str1: str, str2: str) -> str:
    """Add two string and return concatenated string.

    Args:
        a (str): The first string.
        b (str): The second string.

    Returns:
        string: The result of concatenated string  a and b.
    """
    return str1 + str2
